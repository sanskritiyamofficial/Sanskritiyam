// Helper function to get blog by slug
export const getBlogBySlug = (slug) => {
  return blogData.find(blog => blog.slug === slug);
};

// Helper function to get blogs by category
export const getBlogsByCategory = (category) => {
  if (category === 'all') return blogData;
  return blogData.filter(blog => blog.category === category);
};

// Helper function to search blogs
export const searchBlogs = (searchTerm) => {
  if (!searchTerm) return blogData;
  
  const term = searchTerm.toLowerCase();
  return blogData.filter(blog => 
    blog.title.toLowerCase().includes(term) ||
    blog.titleEn.toLowerCase().includes(term) ||
    blog.description.toLowerCase().includes(term) ||
    blog.descriptionEn.toLowerCase().includes(term) ||
    blog.author.toLowerCase().includes(term) ||
    blog.tags.some(tag => tag.toLowerCase().includes(term))
  );
};
